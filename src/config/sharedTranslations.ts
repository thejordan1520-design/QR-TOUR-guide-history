// Función helper para obtener traducción de lugar
export const getLocationTranslation = (locationId: string, field: 'name' | 'description' | 'history', language: string = 'es') => {
  const translations = sharedLocationTranslations[language as keyof typeof sharedLocationTranslations];
  if (!translations || !translations[locationId]) {
    return locationId; // Fallback
  }
  return (translations[locationId] as any)[field] || locationId;
};

// Función para obtener el idioma actual desde localStorage
export const getCurrentLanguage = (): string => {
  const savedLanguage = localStorage.getItem('i18nextLng');
  if (savedLanguage && ['es', 'en', 'fr'].includes(savedLanguage)) {
    return savedLanguage;
  }
  
  const browserLanguage = navigator.language.split('-')[0];
  if (['es', 'en', 'fr'].includes(browserLanguage)) {
    return browserLanguage;
  }
  
  return 'es';
};

// Función para cambiar idioma y guardar en localStorage
export const changeLanguage = (language: string) => {
  localStorage.setItem('i18nextLng', language);
  // Disparar evento para sincronizar con otras páginas
  window.dispatchEvent(new StorageEvent('storage', {
    key: 'i18nextLng',
    newValue: language,
    oldValue: localStorage.getItem('i18nextLng')
  }));
}; 